// Key Components of an API:

// 1. Endpoints: Specific URLs where API requests are sent.

// 2. Methods: The actions that can be performed, such as
// GET (retrieve data), POST (send data), PUT (update data), and DELETE (remove data).

// 3. Requests and Responses: The exchange of data between the client and the server.
// The client makes a request, and the server sends a response.

// 4. Authentication and Authorization: Security measures to ensure that only authorized users can access the API.

